from __future__ import annotations

import csv
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
RESULTS = ROOT / "results"
DOCS = ROOT / "docs"
PAPER = ROOT / "paper"


def read_csv(name: str) -> list[dict[str, str]]:
    with (RESULTS / name).open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def row_count(name: str) -> int:
    return len(read_csv(name))


def find(rows: list[dict[str, str]], **kwargs: str) -> dict[str, str]:
    for row in rows:
        if all(row[k] == v for k, v in kwargs.items()):
            return row
    raise KeyError(kwargs)


def summary_decision() -> str:
    path = RESULTS / "summary.txt"
    for line in path.read_text(encoding="utf-8").splitlines():
        if line.startswith("terminal_decision="):
            return line.split("=", 1)[1].strip()
    return "UNKNOWN"


def tex(text: str) -> str:
    return text.replace("_", r"\_")


def pct(value: str) -> str:
    return f"{100.0 * float(value):.1f}"


def num(value: str) -> str:
    return f"{float(value):.3f}"


def main() -> None:
    aggregate = read_csv("repair_memory_aggregate.csv")
    pairwise = read_csv("repair_memory_pairwise.csv")
    decision = summary_decision()
    corm = find(aggregate, method="corm_repair_memory_v5")
    baselines = [
        "nominal_mpc",
        "robust_worst_case_mpc",
        "last_repair_memory",
        "global_average_repair",
        "knn_repair_memory_v4",
        "online_ridge_residual",
    ]
    lines = [
        "# Paper 63 Final Frozen Evidence Summary",
        "",
        f"Decision: `{decision}`.",
        "",
        "ICLR main readiness: no. The best permissible state is `STRONG_REVISE` because the evidence is CPU-only MuJoCo without hardware or public benchmark confirmation.",
        "",
        "## Frozen Evidence Size",
        "",
        f"- Main raw rows: {row_count('repair_memory_raw.csv')}.",
        f"- Ablation raw rows: {row_count('repair_memory_ablation_raw.csv')}.",
        f"- Metric rows: {row_count('repair_memory_metrics.csv')}.",
        f"- Ablation metric rows: {row_count('repair_memory_ablation.csv')}.",
        f"- Pairwise rows: {row_count('repair_memory_pairwise.csv')}.",
        "- Frozen run: 8 seeds, 32 episodes, 9 main stress splits, 2 ablation splits, 11 main methods.",
        "",
        "## CORM Aggregate",
        "",
        f"- Success: {corm['success_rate']}.",
        f"- Energy regret: {corm['energy_regret_mean']}.",
        f"- Violation rate: {corm['violation_rate']}.",
        f"- Mean trust: {corm['decision_trust_mean']}.",
        f"- Shield activation rate: {corm['shield_activation_rate']}.",
        "",
        "## Strong-Baseline Deltas",
        "",
    ]
    by_method = {row["method"]: row for row in aggregate}
    for baseline in baselines:
        base = by_method[baseline]
        success_delta = float(corm["success_rate"]) - float(base["success_rate"])
        regret_delta = float(base["energy_regret_mean"]) - float(corm["energy_regret_mean"])
        violation_delta = float(corm["violation_rate"]) - float(base["violation_rate"])
        lines.append(
            f"- vs `{baseline}`: success delta {success_delta:.4f}, "
            f"regret improvement {regret_delta:.4f}, violation delta {violation_delta:.4f}."
        )
    lines.extend(["", "## Stress Pairwise Rows", ""])
    for row in pairwise:
        if row["split"] in {"combined_shift", "nonstationary_deployment_shift"} and row["baseline"] in {
            "nominal_mpc",
            "robust_worst_case_mpc",
            "knn_repair_memory_v4",
            "online_ridge_residual",
            "oracle_hidden_deployment",
        }:
            lines.append(
                f"- `{row['split']}` vs `{row['baseline']}`: success delta {row['success_delta_mean']}, "
                f"regret improvement {row['regret_improvement_mean']}, violation delta {row['violation_delta_mean']}, "
                f"regret sign-flip p {row['regret_signflip_p']}."
            )
    lines.extend(
        [
            "",
            "## Honest Interpretation",
            "",
            "The final decision must follow the frozen protocol. If the decision is `STRONG_REVISE`, the result is promising but still not submission-ready for ICLR main without broader benchmarks or hardware. If the decision is `KILL_ARCHIVE`, the manuscript should present the failure as the main contribution.",
            "",
        ]
    )
    out = DOCS / "paper63_final_v5_evidence_summary.md"
    out.write_text("\n".join(lines), encoding="utf-8")
    macros = [
        "% Auto-generated by scripts/summarize_final_decision.py",
        rf"\newcommand{{\TerminalDecision}}{{{tex(decision)}}}",
        rf"\newcommand{{\MainRawRows}}{{{row_count('repair_memory_raw.csv')}}}",
        rf"\newcommand{{\AblationRawRows}}{{{row_count('repair_memory_ablation_raw.csv')}}}",
        rf"\newcommand{{\MetricRows}}{{{row_count('repair_memory_metrics.csv')}}}",
        rf"\newcommand{{\PairwiseRows}}{{{row_count('repair_memory_pairwise.csv')}}}",
        rf"\newcommand{{\CORMAggSuccess}}{{{pct(corm['success_rate'])}}}",
        rf"\newcommand{{\CORMAggRegret}}{{{num(corm['energy_regret_mean'])}}}",
        rf"\newcommand{{\CORMAggViolation}}{{{pct(corm['violation_rate'])}}}",
        rf"\newcommand{{\CORMAggTrust}}{{{num(corm['decision_trust_mean'])}}}",
        rf"\newcommand{{\CORMAggShield}}{{{pct(corm['shield_activation_rate'])}}}",
    ]
    (PAPER / "final_numbers.tex").write_text("\n".join(macros) + "\n", encoding="utf-8")
    print(out)
    print(PAPER / "final_numbers.tex")


if __name__ == "__main__":
    main()
